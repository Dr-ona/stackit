void main() {
  TestWidgetsFlutterBinding.ensureInitialized();

  group('Offline dictionary hit rate', () {
    late OfflineDictionary dict;

    setUpAll(() async {
      dict = OfflineDictionary();
      await dict.load();
    });

    test('EN→AR hit rate is above 95%', () async {
      var hits = 0;
      for (final word in _commonEnglishWords) {
        final result = await dict.lookup(word, LanguagePair.englishToArabic);
        if (result != null) hits++;
      }
      final rate = hits / _commonEnglishWords.length;
      expect(
        rate,
        greaterThanOrEqualTo(0.95),
        reason:
            'EN→AR hit rate ${(rate * 100).toStringAsFixed(1)}% '
            '($hits/${_commonEnglishWords.length}) is below 95%',
      );
    });

    test('AR→EN dictionary has meaningful coverage', () async {
      var hits = 0;
      for (final word in _commonArabicWords) {
        final result = await dict.lookup(word, LanguagePair.arabicToEnglish);
        if (result != null) hits++;
      }
      final rate = hits / _commonArabicWords.length;
      expect(
        rate,
        greaterThanOrEqualTo(0.30),
        reason:
            'AR→EN hit rate ${(rate * 100).toStringAsFixed(1)}% '
            '($hits/${_commonArabicWords.length}) is below 30% minimum',
      );
